                                <h4 class="mb-3">Vivre avec le spina-bifida : Témoignages et Conseils Pratiques</h4>
                                <div class="article-preview">
                                    <p>Découvrez les témoignages inspirants de familles confrontées au spina-bifida. Cet article partage des conseils pratiques pour le quotidien, les soins et l'accompagnement des enfants atteints de cette malformation congénitale...</p>
                                </div>
                                <div class="article-full d-none">
                                    <h5 class="fw-bold mb-3">Introduction</h5>
                                    <p>Le spina-bifida est l'une des malformations congénitales les plus courantes affectant la colonne vertébrale et la moelle épinière. En République Démocratique du Congo, où l'accès aux soins spécialisés reste limité, cette condition représente un défi quotidien pour de nombreuses familles. Cet article se propose d'explorer en profondeur cette pathologie, ses implications et les stratégies pour y faire face.</p>
                                    
                                    <h5 class="fw-bold mt-4 mb-3">Qu'est-ce que le spina-bifida ?</h5>
                                    <p>Le spina-bifida est une malformation congénitale qui survient lors du développement embryonnaire, entraînant une fermeture incomplète du tube neural. Cette affection peut se manifester sous différentes formes, allant des cas les plus bénins aux formes les plus sévères ayant un impact majeur sur la mobilité et les fonctions organiques.</p>
                                    
                                    <h5 class="fw-bold mt-4 mb-3">Les différentes formes de spina-bifida</h5>
                                    <ul class="mb-3">
                                        <li><strong>Spina-bifida occulta</strong> : Forme la plus légère, souvent asymptomatique</li>
                                        <li><strong>Méningocèle</strong> : Les méninges font saillie à travers une ouverture de la colonne</li>
                                        <li><strong>Myéloméningocèle</strong> : Forme la plus sévère où la moelle épinière et les nerfs font saillie</li>
                                    </ul>
                                    
                                    <h5 class="fw-bold mt-4 mb-3">Causes et facteurs de risque</h5>
                                    <p>Plusieurs facteurs peuvent contribuer à la survenue du spina-bifida :</p>
                                    <ul class="mb-3">
                                        <li>Carence en acide folique chez la mère avant et pendant la grossesse</li>
                                        <li>Antécédents familiaux de malformations du tube neural</li>
                                        <li>Certains médicaments anti-épileptiques pendant la grossesse</li>
                                        <li>Diabète maternel mal contrôlé</li>
                                    </ul>
                                    
                                    <h5 class="fw-bold mt-4 mb-3">Complications potentielles</h5>
                                    <p>Les personnes atteintes de spina-bifida peuvent rencontrer diverses complications :</p>
                                    <ul class="mb-3">
                                        <li>Problèmes de mobilité et paralysie partielle ou complète des membres inférieurs</li>
                                        <li>Infections urinaires à répétition et problèmes rénaux</li>
                                        <li>Pertes de sensibilité cutanée, augmentant les risques d'escarres</li>
                                        <li>Hydrocéphalie associée dans de nombreux cas</li>
                                        <li>Difficultés d'apprentissage dans certains cas</li>
                                    </ul>
                                    
                                    <h5 class="fw-bold mt-4 mb-3">Prise en charge et traitements</h5>
                                    <p>La prise en charge du spina-bifida nécessite une approche multidisciplinaire :</p>
                                    <ol class="mb-3">
                                        <li><strong>Chirurgie prénatale ou néonatale</strong> pour fermer l'ouverture de la colonne</li>
                                        <li><strong>Rééducation fonctionnelle</strong> pour améliorer la mobilité et l'autonomie</li>
                                        <li><strong>Suivi urologique</strong> pour prévenir les complications rénales</li>
                                        <li><strong>Appareillage</strong> (fauteuils roulants, attelles) adapté à chaque situation</li>
                                        <li><strong>Suivi orthopédique</strong> pour prévenir les déformations</li>
                                    </ol>
                                    
                                    <h5 class="fw-bold mt-4 mb-3">Conseils pratiques pour les aidants</h5>
                                    <p>Pour les familles accompagnant un enfant atteint de spina-bifida :</p>
                                    <ul class="mb-3">
                                        <li>Apprenez les techniques de soins de base (sondage, prévention des escarres)</li>
                                        <li>Aménagez votre domicile pour faciliter les déplacements</li>
                                        <li>Encouragez l'autonomie tout en restant vigilant</li>
                                        <li>Rejoignez des groupes de soutien pour partager expériences et conseils</li>
                                    </ul>
                                    
                                    <h5 class="fw-bold mt-4 mb-3">Perspectives et espoirs</h5>
                                    <p>Malgré les défis, de nombreux enfants atteints de spina-bifida mènent une vie épanouissante grâce aux progrès de la médecine et au soutien de leur entourage. Les avancées en matière de chirurgie fœtale, de rééducation et d'appareillages offrent des perspectives encourageantes pour améliorer la qualité de vie des personnes concernées.</p>
                                    
